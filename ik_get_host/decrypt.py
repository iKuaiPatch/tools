def init_key():
    result = bytearray(1024)
    step = 16 * 16 * 378551
    for i in range(1024):
        temp = (step + i * step) & 0xFFFFFFFF
        result[i] = (((temp // 0x83) & 0xFF) + key[i % 16]) & 0xFF
    return bytes(result)

def decrypt(key: bytes, data: bytes):
    data_len = len(data)
    result = bytearray(data_len)

    for i in range(data_len):
        temp = (key[i % 1024] * data_len + 16) & 0xFF
        b = (data[i] - temp) & 0xFF
        shift = ((temp + table[temp]) & 7) & 0XFF
        result[i] = ((b << shift) | (b >> (8 - shift))) & 0xFF

    return bytes(result)

