void decrypt(char *key, uint8_t *data, uint32_t data_len) {
    for (uint32_t i = 0; i < data_len; i++) {
        uint8_t temp = key[i % 1024] * data_len + 16;
        uint8_t b = data[i] - temp;
        uint8_t shift = (temp + table[temp]) & 7;
        data[i] = (b << shift) | (b >> (8 - shift));
    }
}

int read_file(const char *filename, uint8_t **data, size_t *size) {
    FILE *file = fopen(filename, "rb");
    if (!file) {
        perror("Error opening input file");
        return -1;
    }
    
    fseek(file, 0, SEEK_END);
    long file_size = ftell(file);
    fseek(file, 0, SEEK_SET);
    
    if (file_size < 0) {
        perror("Error determining file size");
        fclose(file);
        return -1;
    }
    
    *data = malloc(file_size);
    if (!*data) {
        perror("Memory allocation error");
        fclose(file);
        return -1;
    }
    
    size_t bytes_read = fread(*data, 1, file_size, file);
    fclose(file);
    
    *size = bytes_read;
    return 0;
}

