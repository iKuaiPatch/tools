#define __is_print(ch) ((unsigned int)((ch) - ' ') < 127u - ' ')
void hexdump(unsigned char *buf, int size)
{
    int i, j;

    for (i = 0; i < size; i += 16)
    {
        printf("%08X: ", i);

        for (j = 0; j < 16; j++)
        {
            if (i + j < size)
            {
                printf("%02X ", buf[i + j]);
            }
            else
            {
                printf("   ");
            }
        }
        printf(" ");

        for (j = 0; j < 16; j++)
        {
            if (i + j < size)
            {
                printf("%c", __is_print(buf[i + j]) ? buf[i + j] : '.');
            }
        }
        printf("\n");
    }
}

